gunzip -c "$BACKUP_DIR/$BACKUP_FILE.gz" > "$BOT_DIR/bot.db"

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –ø—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞
