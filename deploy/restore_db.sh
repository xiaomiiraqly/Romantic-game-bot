if [ -f "$BOT_DIR/bot.db" ]; then
    cp "$BOT_DIR/bot.db" "$BOT_DIR/bot.db.backup.$(date +%Y%m%d_%H%M%S)"
fi

# –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö
